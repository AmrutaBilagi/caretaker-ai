export const getDynamicResponse = (text, lang = 'en', score = 0) => {
  const lowerText = text.toLowerCase();
  const res = staticResponses[lang] || staticResponses['en'];
  
  const randomChoice = (arr) => arr[Math.floor(Math.random() * arr.length)];
  
  if (lowerText.match(/(suicide|kill myself|end it|want to die|can't go on|give up completely|no point in living|merna|marna|आत्महत्या|मरना|सत्यानाश|ಸಾಯಬೇಕು|ಜೀವನ ಬೇಡ|ಆತ್ಮಹತ್ಯೆ)/i)) {
    return {
      crisis: true,
      text: randomChoice(res.crisis),
      score: -1
    };
  }
  
  let selectedText = "";
  
  if (lowerText.match(/(tired|exhausted|sleepy|thak|थक|ದಣಿವು|ಆಯಾಸ|ನಿದ್ದೆ|ಸುಸ್ತು)/i)) {
    selectedText = randomChoice(res.exhaustion);
  } else if (lowerText.match(/(angry|frustrated|mad|gussa|गुस्सा|कोपा|ಸಿಟ್ಟು|ಕೋಪ)/i)) {
    selectedText = randomChoice(res.anger);
  } else if (lowerText.match(/(lonely|alone|isolated|akela|अकेला|ಒಂಟಿ|ಒಂಟಿತನ)/i)) {
    selectedText = randomChoice(res.loneliness);
  } else if (lowerText.match(/(anxious|worry|scared|chinta|चिंता|ಡರ್|ಭಯ|ಆತಂಕ)/i)) {
    selectedText = randomChoice(res.anxiety);
  } else if (lowerText.match(/(happy|good|relieved|khush|खुश|ಖುಷಿ|ಸಂತೋಷ|ಒಳ್ಳೆಯ)/i)) {
    selectedText = randomChoice(res.joy);
  } else {
    if (score >= 0.05) {
      selectedText = randomChoice(res.neutralPositive);
    } else if (score <= -0.05) {
      selectedText = randomChoice(res.neutralNegative);
    } else {
      selectedText = randomChoice(res.general);
    }
  }
  
  return { crisis: false, text: selectedText, score: score };
};
